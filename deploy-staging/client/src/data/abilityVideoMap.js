export function getAbilityVideosForDimension(name) {
  const normalizedName = String(name || '').trim();

  if (abilityVideoMap[normalizedName]) {
    return abilityVideoMap[normalizedName];
  }

  const matchedRule = fuzzyRules.find((rule) => rule.keywords.some((keyword) => normalizedName.includes(keyword)));
  if (matchedRule && abilityVideoMap[matchedRule.target]) {
    return abilityVideoMap[matchedRule.target];
  }

  return defaultAbilityVideos;
}
